#!/usr/bin/env node
// scripts/seed-leadership-scenarios.js
// Same pattern as seed-scenarios.js, pointed at LEADERSHIP_SCENARIOS instead
// of SCENARIOS. Kept as a separate script rather than merging into
// seed-scenarios.js so the two content tracks (client-communication vs.
// leadership-and-operations) can be seeded/re-run independently.

const fs = require('fs')
const path = require('path')
const Module = require('module')

function loadDataModule() {
  const dataPath = path.join(__dirname, '..', 'lib', 'data.js')
  let src = fs.readFileSync(dataPath, 'utf8')
  src = src.replace(/^export const /gm, 'const ')
  src = src.replace(/^export async function/gm, 'async function')
  src = src.replace(/^export function/gm, 'function')
  src += '\nmodule.exports = { LEADERSHIP_SCENARIOS }'
  const m = new Module(dataPath, module)
  m.filename = dataPath
  m.paths = Module._nodeModulePaths(path.dirname(dataPath))
  m._compile(src, dataPath)
  return m.exports
}

const { LEADERSHIP_SCENARIOS } = loadDataModule()

if (!Array.isArray(LEADERSHIP_SCENARIOS) || LEADERSHIP_SCENARIOS.length === 0) {
  console.error('No LEADERSHIP_SCENARIOS found in lib/data.js — nothing to seed.')
  process.exit(1)
}

// Same fixed system-author identity used by seed-scenarios.js.
const SYSTEM_USER_ID = '00000000-0000-0000-0000-000000000003'

function dollarQuote(str) {
  if (str === null || str === undefined) return 'NULL'
  let tag = 'CIQL'
  let n = 0
  while (str.includes(`$${tag}$`)) { n += 1; tag = `CIQL${n}` }
  return `$${tag}$${str}$${tag}$`
}
function jsonbLiteral(value) {
  if (value === null || value === undefined) return 'NULL'
  return `${dollarQuote(JSON.stringify(value))}::jsonb`
}
const EVEN_RUBRIC_WEIGHTING = { clarity: 1, data: 1, ownership: 1, tone: 1, commitment: 1 }

const parts = []
parts.push(`-- Seed: scenario_families / scenario_versions from lib/data.js LEADERSHIP_SCENARIOS
-- Generated by scripts/seed-leadership-scenarios.js
-- ${new Date().toISOString()}
-- Assumes the system library-author identity from seed-scenarios.js already exists.
`)

for (const s of LEADERSHIP_SCENARIOS) {
  const slug = s.id.toLowerCase()
  const rolePersona = (() => {
    const emdash = s.counterpartPersona.indexOf('\u2014')
    if (emdash > 0 && emdash < 120) return s.counterpartPersona.slice(0, emdash).trim()
    const firstSentence = s.counterpartPersona.split(/[.\n]/)[0].trim()
    return firstSentence.length <= 120 ? firstSentence : firstSentence.slice(0, 117) + '...'
  })()

  parts.push(`-- ─── ${s.id} · ${s.title} ───
DO $$
DECLARE
  fam_id UUID;
  ver_id UUID;
BEGIN
  SELECT id INTO fam_id FROM scenario_families WHERE org_id IS NULL AND slug = ${dollarQuote(slug)};
  IF fam_id IS NULL THEN
    INSERT INTO scenario_families (org_id, slug, industry_id, training_type)
    VALUES (NULL, ${dollarQuote(slug)}, ${dollarQuote(s.industry)}, ${dollarQuote(s.trainingType)})
    RETURNING id INTO fam_id;
  END IF;

  IF NOT EXISTS (SELECT 1 FROM scenario_versions WHERE family_id = fam_id) THEN
    INSERT INTO scenario_versions (
      family_id, version_number, status, title, day_label, difficulty, role_persona,
      context, opening_line, client_persona, data_packet, success_criteria, rubric_weighting,
      authored_by, approved_by, approved_at, change_notes
    ) VALUES (
      fam_id, 1, 'approved',
      ${dollarQuote(s.title)},
      ${dollarQuote(s.day)},
      ${dollarQuote(s.difficulty)},
      ${dollarQuote(rolePersona)},
      ${dollarQuote(s.context)},
      ${dollarQuote(s.openingLine)},
      ${dollarQuote(s.counterpartPersona)},
      ${jsonbLiteral(s.dataPacket)},
      ${jsonbLiteral(s.successCriteria)},
      ${jsonbLiteral(EVEN_RUBRIC_WEIGHTING)},
      '${SYSTEM_USER_ID}', '${SYSTEM_USER_ID}', now(),
      $CIQL$Seeded from lib/data.js LEADERSHIP_SCENARIOS array (initial migration)$CIQL$
    )
    RETURNING id INTO ver_id;

    UPDATE scenario_families SET current_version_id = ver_id WHERE id = fam_id;
  END IF;
END $$;
`)
}

const sql = parts.join('\n')
const outDir = path.join(__dirname, 'output')
fs.mkdirSync(outDir, { recursive: true })
const outPath = path.join(outDir, 'seed-leadership-scenarios.sql')
fs.writeFileSync(outPath, sql)
console.error(`Wrote ${LEADERSHIP_SCENARIOS.length} leadership scenarios to ${outPath}`)
